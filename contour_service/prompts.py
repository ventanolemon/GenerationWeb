GENERATOR_REPAIR_RULES = """\
РЕЖИМ ИСПРАВЛЕНИЯ: если сообщение пользователя — JSON с полями previous_graph
и errors, это repair-запрос. Верни исправленный ПОЛНЫЙ граф тем же форматом
(не диф, не фрагмент). Меняй только то, что необходимо для устранения
перечисленных ошибок; тексты ошибок точны и называют узлы по id."""


def build_generator_system(catalog_version: str, catalog_text: str,
                           few_shots: list) -> str:
    """Системный промпт S1 (training_plan.md §2). few_shots — FewShotExample."""
    shots = []
    for ex in few_shots:
        shots.append(
            f"Описание: {ex.description}\n"
            f"Граф: {json.dumps(ex.graph, ensure_ascii=False)}"
        )
    shots_block = ("\n\n=== ПРИМЕРЫ ===\n" + "\n\n".join(shots)) if shots else ""
    return (
        "Ты — компилятор описаний учебных заданий в JSON-граф (GraphSpec). "
        "Отвечай только JSON.\n\n"
        f"=== КАТАЛОГ УЗЛОВ (catalog_version: {catalog_version}) ===\n"
        f"{catalog_text}\n\n"
        f"{GENERATOR_FORMAT_RULES}\n\n"
        f"{GENERATOR_REPAIR_RULES}"
        f"{shots_block}"
    )


def build_repair_message(round_no: int, stage: str, errors: list[dict],
                         previous_graph: dict) -> dict:
    """Repair-сообщение — ДОСЛОВНО формат closed_loop_contract.md §2.

    errors: [{"text": дословный текст ошибки/fix_hint, "seed": int|None,
              "code": str|None}] — все ошибки раунда одним сообщением.
    previous_graph — ПОЛНЫЙ JSON предыдущей попытки, не диф."""
    return {
        "round": round_no,
        "stage": stage,                       # build | execute | probe | critic
        "errors": errors,
        "previous_graph": previous_graph,
        "instruction": ("Верни исправленный ПОЛНЫЙ граф тем же форматом. "
                        "Меняй только необходимое."),
    }


