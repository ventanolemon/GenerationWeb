def apply_descriptions(registry) -> None:
    """Проставить описания классам узлов, у которых нет собственного атрибута."""
    for cls in registry:
        if not getattr(cls, "description", ""):
            desc = DESCRIPTIONS.get(cls.type_id)
            if desc:
                cls.description = desc
